/**
 * @file app_cursor.h
 * @brief 多舵机光标选择与电位器锁定控制
 * @author 李嘉图
 * @date 2026-06-03
 *
 * @note 管理一个全局光标，在多个舵机间循环切换。
 *       同时提供电位器锁定功能，锁定后旋钮不再控制当前舵机，
 *       仅允许 +/- 按键微调。
 */

#ifndef __APP_CURSOR_H__
#define __APP_CURSOR_H__

/*==============================================================================
 * 头文件包含
 *============================================================================*/
#include <stdint.h>

/*==============================================================================
 * 宏定义与常量
 *============================================================================*/
#define CURSOR_SERVO_NUM    3                           // 舵机总数量
#define CURSOR_SERVO_IDS    {1, 2, 3}                   // 舵机ID列表

/*==============================================================================
 * 光标结构体定义
 *============================================================================*/
typedef struct {
    uint8_t index;              // 光标索引（0 ~ CURSOR_SERVO_NUM-1）
    uint8_t current_id;         // 当前选中的舵机ID（1/2/3）
    uint8_t lock;               // 电位器锁定标志：1=锁定，0=启用
} APP_Cursor_t;

/*==============================================================================
 * 全局变量
 *============================================================================*/
extern APP_Cursor_t g_cursor;

/*==============================================================================
 * 功能函数
 *============================================================================*/
/**
 * @brief 初始化光标
 * @note  默认光标指向第 1 个舵机，电位器未锁定
 */
void APP_Cursor_Init(void);

/**
 * @brief 光标向前切换（1→2→3→1…）
 */
void APP_Cursor_Next(void);

/**
 * @brief 光标向后切换（1→3→2→1…）
 */
void APP_Cursor_Prev(void);

/**
 * @brief 反转电位器锁定状态（锁定↔解锁）
 */
void APP_Cursor_ToggleLock(void);

#endif
