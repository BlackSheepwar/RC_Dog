#include "app_usart.h"

/*==============================================================================
 * 任务函数
 *============================================================================*/
void Task_TX(void *argument)
{
  for(;;)
  {
    osSemaphoreAcquire(TX_BSHandle, osWaitForever);
    APP_USART_SendTxPacket();
  }
}