#ifndef __COMMON_H__
#define __COMMON_H__

/*==============================================================================
 * 头文件包含
 *============================================================================*/
// 固定包含
#include <stdint.h>
#include <stddef.h>

/*==============================================================================
 * 平台抽象
 *============================================================================*/
/* 计算静态数组元素个数 */
#define ARRAY_SIZE(arr)     (sizeof(arr) / sizeof((arr)[0]))

/*==============================================================================
 * 通用返回状态
 *============================================================================*/
typedef enum {
    ERR_OK      =  0,    // 成功
    ERR_PARAM   = -1,    // 参数错误
    ERR_BUSY    = -2,    // 资源忙
    ERR_TIMEOUT = -3,    // 超时
    ERR_LOCKED  = -4,    // 全局锁定（紧急停止/致命错误）
    ERR_UNKNOWN = -5,    // 未知错误
} ErrorCode_t;

#endif /* __COMMON_H__ */
