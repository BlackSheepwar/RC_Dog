/**
 * @file bsp_gpio.h
 * @brief GPIO 通用 BSP 层（通过逻辑ID映射到GPIO引脚）
 * @author 李嘉图
 * @date 2026-06-17
 *
 * @note 适用于按键、开关、编码器、传感器数字IO、LED、继电器等
 *       所有需要 GPIO 输入/输出的场景。
 *
 *       硬件映射表（gpio_id → GPIOx + Pin）内置在 bsp_gpio.c 中，
 *       上层调用时只需传 gpio_id，无需知道具体 GPIO 端口和引脚。
 *       设计模式同 bsp_pwm.h，硬件细节对上层完全透明。
 *
 *       对于输出引脚，CubeMX 必须已将其配置为输出模式。
 */

#ifndef __BSP_GPIO_H__
#define __BSP_GPIO_H__

/*==============================================================================
 * 头文件包含
 *============================================================================*/
#include <stdint.h>
#include "gpio.h"

/*==============================================================================
 * 宏定义与常量
 *============================================================================*/
#define GPIO_MAX_NUM    20    /* 最大支持 GPIO 点数（含未使用槽位） */

#define GPIO_LEVEL_LOW  0    /* 低电平 */
#define GPIO_LEVEL_HIGH 1    /* 高电平 */

/*==============================================================================
 * 初始化
 *============================================================================*/
/**
 * @brief GPIO BSP 层初始化
 */
void BSP_GPIO_Init(void);

/*==============================================================================
 * 数字输入
 *============================================================================*/
/**
 * @brief 读取 GPIO 引脚电平
 * @param gpio_id 逻辑编号（对应 GPIO_HW_MAP 中的映射）
 * @retval GPIO_LEVEL_HIGH (1): 高电平
 * @retval GPIO_LEVEL_LOW  (0): 低电平
 * @retval 0xFF: 未找到对应引脚
 */
uint8_t BSP_GPIO_ReadLevel(uint8_t gpio_id);

/*==============================================================================
 * 数字输出
 *============================================================================*/
/**
 * @brief 设置 GPIO 引脚电平
 * @param gpio_id 逻辑编号
 * @param level   电平：GPIO_LEVEL_HIGH 或 GPIO_LEVEL_LOW
 * @note 引脚必须在 CubeMX 中配置为输出模式，否则无效
 */
void BSP_GPIO_SetLevel(uint8_t gpio_id, uint8_t level);

/**
 * @brief 翻转 GPIO 引脚电平
 * @param gpio_id 逻辑编号
 * @note 高→低 或 低→高
 */
void BSP_GPIO_Toggle(uint8_t gpio_id);

/*==============================================================================
 * 查询
 *============================================================================*/
/**
 * @brief 检查 gpio_id 是否在映射表中有效
 * @param gpio_id 逻辑编号
 * @retval 1: 有效
 * @retval 0: 无效（未在映射表中）
 */
uint8_t BSP_GPIO_IsValid(uint8_t gpio_id);

#endif /* __BSP_GPIO_H__ */
