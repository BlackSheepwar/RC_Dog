/**
 * @file app_limb_cfg.h
 * @brief 肢体→舵机映射配置表
 * @author 李嘉图
 * @date 2026-06-12
 *
 * @note 将肢体层的舵机映射提取为独立配置，
 *       通过编译期 const 表定义肢体与舵机 ID 的对应关系。
 *       与 app_servo_cfg.h 同理，不同硬件版本可配不同映射表。
 *
 *       索引：[肢体编号][关节] → 舵机 ID
 *       每个肢体 LIMB_JOINT_COUNT 个关节，顺序为 髋, 膝。
 *       未使用的肢体舵机 ID 填 0（APP_Servo 会忽略无效 ID）。
 */

#ifndef __APP_LIMB_CFG_H__
#define __APP_LIMB_CFG_H__

#include <stdint.h>
#include "app_limb.h"

/*==============================================================================
 * 舵机跟随速度
 *============================================================================*/
/** @brief 舵机快速跟随速度（Limb 每 tick 更新目标，舵机以此速度追上） */
#define LIMB_SERVO_SPEED_DPS  5000.0f

/*==============================================================================
 * 舵机→肢体映射（编译期常量）
 *
 *         髋关节  膝关节
 *  肢体 0:  ID 1   ID 2
 *  肢体 1:  ID 3   ID 4
 *  肢体 2:  —未使用—
 *  肢体 3:  —未使用—
 *============================================================================*/
static const uint8_t LIMB_SERVO_MAP[LIMB_MAX][LIMB_JOINT_COUNT] = {
    {1, 2},   /* 肢体 0: 髋 → ID 1, 膝 → ID 2 */
    {3, 4},   /* 肢体 1: 髋 → ID 3, 膝 → ID 4 */
    {0, 0},   /* 肢体 2: 未使用 */
    {0, 0},   /* 肢体 3: 未使用 */
};

#endif /* __APP_LIMB_CFG_H__ */
