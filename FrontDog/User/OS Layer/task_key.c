#include "main.h"
#include "app_key.h"

/*==============================================================================
 * 任务函数
 *============================================================================*/
void Task_KEY(void *argument)
{
  App_Key_Init();
  App_Key_Register(1, 18, 1, GPIOC, GPIO_PIN_13);
  for(;;)
  {
    App_Key_Update();
    osDelay(10);
  }
}