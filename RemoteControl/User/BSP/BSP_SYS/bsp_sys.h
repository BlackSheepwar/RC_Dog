#ifndef __BSP_SYS_H__
#define __BSP_SYS_H__

/*==============================================================================
 * 头文件包含
 *============================================================================*/
// 固定包含
#include <stdint.h>

/*==============================================================================
 * 时间基准
 *============================================================================*/
/**
 * @brief 获取系统滴答计数值（单位：ms）
 * @return 系统上电后的毫秒数（49 天回绕）
 * @note 实质为 HAL_GetTick() 的 BSP 层封装。
 *       使用无符号减法可安全处理回绕：
 *       if ((now - last) > timeout)  // 正确处理回绕
 */
uint32_t BSP_GetTickMs(void);

#endif /* __BSP_SYS_H__ */
