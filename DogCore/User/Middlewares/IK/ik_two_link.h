/**
 * @file ik_two_link.h
 * @brief 二连杆逆运动学解算
 * @author 李嘉图
 * @date 2026-06-11
 *
 * @note 输入足端在髋关节坐标系下的模型坐标 (x, y)，
 *       输出髋/膝关节角度（弧度）。
 *       坐标系：原点在髋关节，+x 前，+y 下（垂直身体平面）。
 *       注意：x 是模型内部坐标（含髋膝耦合分量），不代表物理前后偏移。
 *       标定：servo(0,90)↔(0,260), servo(-77,-63)↔(-57.5,27.4)
 */

#ifndef __IK_TWO_LINK_H__
#define __IK_TWO_LINK_H__

/*==============================================================================
 * 头文件包含
 *============================================================================*/
#include <stdint.h>

/*==============================================================================
 * 宏定义
 *============================================================================*/
#define RAD_TO_DEG     57.29577951308232f
#define DEG_TO_RAD     0.017453292519943f

/*==============================================================================
 * IK 解算（弧度输出）
 *============================================================================*/
/**
 * @brief 二连杆 IK 解算（输出弧度）
 * @param L1    大腿长度(mm)
 * @param L2    小腿长度(mm)
 * @param x     足端 x 坐标（+x 前）
 * @param y     足端 y 坐标（+y 下）
 * @param hip   输出：髋关节角度(rad)
 * @param knee  输出：膝关节角度(rad)
 * @return 1=可达，0=不可达
 */
uint8_t IK_TwoLink(float L1, float L2, float x, float y,
                   float *hip, float *knee);

/*==============================================================================
 * IK 解算（角度输出）
 *============================================================================*/
/**
 * @brief 二连杆 IK 解算（输出角度）
 * @param L1       大腿长度(mm)
 * @param L2       小腿长度(mm)
 * @param x        足端 x 坐标（+x 前）
 * @param y        足端 y 坐标（+y 下）
 * @param hip_deg  输出：髋关节角度(°)
 * @param knee_deg 输出：膝关节角度(°)
 * @return 1=可达，0=不可达
 * @note 推荐使用此版本，输出可直接传给 Motion_SetTarget
 */
uint8_t IK_TwoLink_Deg(float L1, float L2, float x, float y,
                        float *hip_deg, float *knee_deg);

#endif /* __IK_TWO_LINK_H__ */
